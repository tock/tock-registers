#[cfg(target_pointer_width = "64")]
fn main() {
    // Setup: create a fake peripheral (we don't need behavior, so using Real with a local buffer
    // is fine), construct the UartDma, and allocate 'static buffers.
    let peripheral = UnsafeCell::new([0usize; 6]);
    let mmio = Mmio64::new(NonNull::new(peripheral.get()).unwrap().cast());
    // Safety: Our fake peripheral is a good-enough model of the theoretical UART.
    let registers = unsafe { uart::Real::new(mmio) };
    // Safety: only calling this once.
    let uart_dma = unsafe { UartDma::new(registers) };
    // Allocate 'static buffers.
    let buffer1 = Box::leak(Box::new([1, 2, 3, 4]));
    let buffer2 = Box::leak(Box::new([5, 6, 7, 8]));

    // Demonstrate running receive and transmit operations using DMA.
    let uart_dma = uart_dma.start_rx(buffer1);
    let uart_dma = uart_dma.start_tx(buffer2);
    // Demonstrate that we can move the typestate into runtime states. This is how driver structs
    // would likely store the UartDma.
    let uart_dma: UartDma<uart::Real, Dynamic, Dynamic> = uart_dma.into_dynamic();
    // Convert the runtime states back into typestates, stop the operations, and clean up the
    // buffers.
    let uart_dma = uart_dma
        .into_static()
        .unwrap_or_else(|_| panic!("wrong state"));
    let (uart_dma, buffer1) = uart_dma.stop_rx();
    let (_, buffer2) = uart_dma.stop_tx();

    // Deallocate the buffers.
    let _ = unsafe { Box::from_raw(buffer1) };
    let _ = unsafe { Box::from_raw(buffer2) };
}

#[cfg(not(target_pointer_width = "64"))]
fn main() {}
